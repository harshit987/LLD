public class Location {
    private int x;
    private int y;
    private int floorNo;

    public Location(int x, int y, int floorNo) {
        this.x = x;
        this.y = y;
        this.floorNo = floorNo;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getFloorNo() {
        return floorNo;
    }
}
